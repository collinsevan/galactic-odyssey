def intro_story():
    return (
        "You are the captain of the spaceship *Starfire*, assigned to deliver "
        "critical supplies to the colony on Titan V. Your journey through deep "
        "space will be fraught with danger. Stay sharp and make the right choices "
        "to complete your mission."
    )

def prepare_ship_story():
    return (
        "As you begin preparations for launch, you notice a series of system alerts. "
        "The fuel line has a leak, and the navigation system needs recalibration. "
        "Quick decisions and precise actions are needed to avoid delays."
    )

def review_mission_story():
    return (
        "You decide to review the mission parameters and double-check the cargo "
        "manifest. Everything must be accounted for, and no errors can be tolerated "
        "if the mission is to succeed. Your careful review ensures that all systems "
        "are operational."
    )

def equipment_malfunction_story():
    return (
        "Just as you settle into the journey, a critical alert sounds. The life support "
        "system has malfunctioned, threatening the air supply. Immediate action is "
        "required to ensure the safety of everyone aboard."
    )

